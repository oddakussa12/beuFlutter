///抖动类型
///[ShakeType.LeftRight]左右抖动
///[ShakeType.TopBottom]上下抖动
///[ShakeType.Skew]斜角抖动
///[ShakeType.Rotate]旋转抖动
///[ShakeType.Random]随机抖动
///
enum ShakeType {
  /// 左右抖动
  LeftRight,

  /// 上下抖动
  TopBottom,

  /// 斜角抖动
  Skew,

  /// 旋转抖动
  Rotate,

  /// 随机抖动
  Random,
}
